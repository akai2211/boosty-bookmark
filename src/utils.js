// Проверка, жив ли контекст расширения (не был ли он перезагружен/обновлён)
function isExtensionContextValid() {
  try {
    return !!chrome.runtime && !!chrome.runtime.id;
  } catch (e) {
    return false;
  }
}

// Форматирование Unix-таймстампа (в секундах) в дату вида «5 мая 2024»
function formatDate(timestamp) {
  const date = new Date(timestamp * 1000);
  const monthStr = t('month_' + date.getMonth());
  return `${date.getDate()} ${monthStr} ${date.getFullYear()}`;
}

// Сравнение двух постов на эквивалентность (тайтл, лайк, уровень подписки, теги)
function arePostsEqual(p1, p2) {
  if (p1.title !== p2.title) return false;
  if (p1.isLiked !== p2.isLiked) return false;

  const sub1 = p1.subscriptionLevel;
  const sub2 = p2.subscriptionLevel;
  if ((sub1 && !sub2) || (!sub1 && sub2)) return false;
  if (sub1 && sub2 && sub1.id !== sub2.id) return false;

  if (p1.tags.length !== p2.tags.length) return false;
  const tags1 = p1.tags.map(t => t.id).sort();
  const tags2 = p2.tags.map(t => t.id).sort();
  for (let i = 0; i < tags1.length; i++) {
    if (tags1[i] !== tags2[i]) return false;
  }

  return true;
}

// Красивое форматирование секунд в ММ:СС или ЧЧ:ММ:СС
function formatSeconds(seconds) {
  if (isNaN(seconds) || seconds === null || seconds === undefined) return '0:00';
  const hrs = Math.floor(seconds / 3600);
  const mins = Math.floor((seconds % 3600) / 60);
  const secs = Math.floor(seconds % 60);
  const formattedSecs = secs < 10 ? '0' + secs : secs;
  if (hrs > 0) {
    const formattedMins = mins < 10 ? '0' + mins : mins;
    return `${hrs}:${formattedMins}:${formattedSecs}`;
  }
  return `${mins}:${formattedSecs}`;
}

// Форматирование даты последней WebDAV-синхронизации с учётом языка интерфейса
function formatSyncDate(timestamp) {
  if (!timestamp) return t('settings_webdav_never_sync');
  const date = new Date(timestamp);
  const isEn = (typeof getCurrentLang === 'function' ? getCurrentLang() : 'ru') === 'en';
  const dateLocale = isEn ? 'en-US' : 'ru-RU';
  return date.toLocaleString(dateLocale, {
    day: 'numeric',
    month: 'short',
    year: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  });
}

// Бинарные данные <-> base64 для передачи тела WebDAV через chrome.runtime.sendMessage
// (сообщения сериализуются как JSON, ArrayBuffer не переживает пересылку). Base64-строка
// в разы компактнее массива байт-чисел по памяти и скорости сериализации. Кодируем
// чанками, чтобы не переполнить стек на String.fromCharCode при больших архивах.
function arrayBufferToBase64(buffer) {
  const bytes = new Uint8Array(buffer);
  let binary = '';
  const CHUNK = 0x8000;
  for (let i = 0; i < bytes.length; i += CHUNK) {
    binary += String.fromCharCode.apply(null, bytes.subarray(i, i + CHUNK));
  }
  return btoa(binary);
}

function base64ToArrayBuffer(base64) {
  const binary = atob(base64);
  const len = binary.length;
  const bytes = new Uint8Array(len);
  for (let i = 0; i < len; i++) bytes[i] = binary.charCodeAt(i);
  return bytes.buffer;
}

